package com.green.practice.day05.ch06.ex;

public class ex06_1 {

    public static void main(String[] args) {
        simpleOpr(10,2);
    }

    static void simpleOpr(int n1, int n2) {
        System.out.printf("%s + %s = %s\n", n1, n2, (n1 + n2));
        System.out.printf("%s - %s = %s\n", n1, n2, (n1 - n2));
        System.out.printf("%s * %s = %s\n", n1, n2, (n1 * n2));
        System.out.printf("%s %% %s = %s\n", n1, n2, (n1 % n2));

    }





}
