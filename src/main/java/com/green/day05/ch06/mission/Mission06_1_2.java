package com.green.day05.ch06.mission;

public class Mission06_1_2 {

    public static void main(String[] args) {
        result(5, 10);
    }

    static void result(int n1, int n2) {
        if(n1 > n2) {
            System.out.println(n1 - n2);
        }else {
            System.out.println(n2 - n1);
        }
    }





}
